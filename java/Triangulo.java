
public class Triangulo {
	private int x0, x1, y0, y1 = 0;

	public int getX0() {
		return x0;
	}//END FUNCTION

	public void setX0(int x0) {
		this.x0 = x0;
	}//END FUNCTION

	public int getX1() {
		return x1;
	}//END FUNCTION

	public void setX1(int x1) {
		this.x1 = x1;
	}//END FUNCTION

	public int getY0() {
		return y0;
	}//END FUNCTION

	public void setY0(int y0) {
		this.y0 = y0;
	}//END FUNCTION

	public int getY1() {
		return y1;
	}//END FUNCTION

	public void setY1(int y1) {
		this.y1 = y1;
	}//END FUNCTION
	
}//END CLASS
